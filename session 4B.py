#  bitwise operator

num1 = 12
num2 = 10
print("binary of num1 is",bin(num1))
print("binary of num2 is",bin(num2))

num3 = num1 & num2
print("value of num3 is",num3,bin(num3))

num4 = num1 | num2
print ('value of  or operator of num1 and num 2',num4 ,bin(num4))

num5 = num1>>2
print('num 5 is ',bin(num5))
num6 = num1<<2
print('num 6 is ',bin(num6))


num7 = -19
print("num7 is",bin(num7))

num8 = num7<<4
print("num8 is", num8,bin(num8))

num9 = -12
print("num9 is",bin(num9))

num10 = num9>>3
print("num10 is", num10,bin(num10))
